    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        
        
        if (counter < count ){
        counter = counter + 1;
        jLabelQ.setText("Question "+ counter +":");
        totalScore();
        if(counter == count-1){
            jButtonNext.setText("Finish");
            counter = counter + 1;
        }
        
    
        checkId(id, setid);
        buttonGroup1.clearSelection();
        System.out.println("count: "+ count + " counter: " + counter + " id: " + id + " score: " + score );
        } 
        
        else if (counter == count){
            totalScore();
            storeResult();
            JOptionPane.showMessageDialog(null, storeResult());
            ResultJFrame result = new ResultJFrame(score, examineeid, setid);
            result.setVisible(true);
            dispose();
        }
        
  
    }//GEN-LAST:event_jButtonNextActionPerformed

    
    public Boolean storeResult(){ 
        String query = "INSERT INTO `result` (`examineeid`, `score`, `setname`) VALUES( '"+ examineeid + "', '" + score  + "', '" + setname +"')";         
        System.out.println(query);
        return mySqlConn.executeSQLQuery(query, "Updated");

    }
    
    public Boolean retrieveSetname(int qsetid) {
        String query = "SELECT setname FROM questionset where id = " + qsetid;
        ResultSet resultset;
        resultset = mySqlConn.retrieveData(query);
        try{
            if (resultset.next()){
                setname = resultset.getString("setname");
                return true;
            } 
            
        } catch (SQLException ex){
            System.out.println("Setname Not retrieved: " + ex);
        }
        return false;
    }
    
