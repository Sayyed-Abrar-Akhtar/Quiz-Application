    public int countQuestion(int setid) {
        
        String query = "SELECT COUNT(id) AS 'totalq' FROM questions where setid = " + setid;
        ResultSet resultset;
        resultset = mySqlConn.retrieveData(query);
        try{
            if (resultset.next()){
                count = resultset.getInt("totalq");
            } 
            
        } catch (SQLException ex){
            System.out.println("Error in loadData method in line 251: " + ex);
        }
        return count;
    }
    
    private void checkId(int qtid, int setid) {
        String query = "select * from questions where id = " + qtid +" and setid = " + setid;
        ResultSet resultset;
        resultset = mySqlConn.retrieveData(query);
        try{
            if (resultset.next()){
                qid = id;
                loadData(qid, setid);
                id = id+1;
            } else {
                id = id+1;
                checkId(id, setid);
            }
            
        } catch (SQLException ex){
            System.out.println("Error in loadData method in line 251: " + ex);
        }
    }
    
    private void loadData(int id, int setid){
        String query = "select * from questions where id = " + id +" and setid = " + setid;
        ResultSet resultset; 
        
        
        resultset = mySqlConn.retrieveData(query);
        try{
        if (resultset.next()){
            jTextAreaQ.setText(resultset.getString("question"));
            jTextAreaQ.setEditable(false);
            jRadioButtonO1.setText(resultset.getString("a"));
            jRadioButtonO2.setText(resultset.getString("b"));
            jRadioButtonO3.setText(resultset.getString("c"));
            jRadioButtonO4.setText(resultset.getString("d"));
            jRadioButtonO5.setText(resultset.getString("e"));
            correctans = resultset.getString("correctanswer");
           
        }
            
        } catch (SQLException ex){
            System.out.println("Error in loadData method in line 251: " + ex);
        }
        
    }
  
    
    
